/* eslint-disable no-unused-vars */

/**
 * Add two numbers
 * @param {Number} a The first number
 * @param {Number} b The second number
 */
export const add = (a, b) => {
    return a + b
}

export const subtract = (a, b) => {
    return a - b
}

export const multiply = (a, b) => {
    return a * b
}
